package com.xworkz.TaskOnInheritance.Catering;

public class Catering {
	public String name;
	public int inOfWorkers;
	public boolean tastesGood;

	public Catering() {
		super();
	}

	public Catering(String name, int inOfWorkers, boolean tastesGood) {
		super();
		this.name = name;
		this.inOfWorkers = inOfWorkers;
		this.tastesGood = tastesGood;
	}

}
