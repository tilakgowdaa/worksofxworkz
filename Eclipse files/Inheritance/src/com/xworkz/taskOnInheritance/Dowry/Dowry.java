package com.xworkz.taskOnInheritance.Dowry;

public class Dowry {
	public boolean taken;
	public boolean isLegal;
	public int prohibitionAct;

	public Dowry() {
		super();
	}

	public Dowry(boolean taken, boolean isLegal, int prohibitionAct) {
		super();
		this.taken = taken;
		this.isLegal = isLegal;
		this.prohibitionAct = prohibitionAct;
	}

}
