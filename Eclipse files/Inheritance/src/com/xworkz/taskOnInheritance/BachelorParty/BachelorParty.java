package com.xworkz.taskOnInheritance.BachelorParty;

public class BachelorParty {
	public String location;
	public String brand;
	public int noOfPeoples;

	public BachelorParty() {
		super();
	}

	public BachelorParty(String location, String brand, int noOfPeoples) {
		super();
		this.location = location;
		this.brand = brand;
		this.noOfPeoples = noOfPeoples;
	}

}
