package com.xworkz.taskOnInheritance.Marriage;

public class Marriage {
	public String date;
	public String venue;
	public int noOfMarriage;

	public Marriage() {
		super();
	}

	public Marriage(String date, String venue, int noOfMarriage) {
		super();
		this.date = date;
		this.venue = venue;
		this.noOfMarriage = noOfMarriage;
	}

}
