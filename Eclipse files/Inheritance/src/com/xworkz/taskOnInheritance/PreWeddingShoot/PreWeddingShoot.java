package com.xworkz.taskOnInheritance.PreWeddingShoot;

public class PreWeddingShoot {
	public String location;
	public double price;
	public String photographerName;

	public PreWeddingShoot() {
		super();
	}

	public PreWeddingShoot(String location, double price, String photographerName) {
		super();
		this.location = location;
		this.price = price;
		this.photographerName = photographerName;
	}

}
