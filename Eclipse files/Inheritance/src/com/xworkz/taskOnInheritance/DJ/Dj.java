package com.xworkz.taskOnInheritance.DJ;

public class Dj {
	public String name;
	public String showLocation;
	public String timings;

	public Dj() {
		super();
	}

	public Dj(String name, String showLocation, String timings) {
		super();
		this.name = name;
		this.showLocation = showLocation;
		this.timings = timings;
	}

}
